"""
Granite ASR adapter for unified ASR pipeline.
Uses Granite speech for transcription/translation and optionally Qwen forced aligner for timestamps.
"""

from typing import Any, Dict, List, Optional, Tuple

import torch
import torch.nn.functional as F
import comfy.model_management as model_management
import comfy.utils
import time
import re

from engines.granite_asr.prompting import DEFAULT_TRANSLATE_INSTRUCTION_TEMPLATE
from utils.asr.types import ASRRequest, ASRResult, ASRSegment, ASRWord


def parse_granite_native_timestamps(ts_text: str, chunk_offset: float) -> Tuple[str, List[ASRSegment]]:
    """
    Parses native timestamped text generated by ibm-granite/granite-speech-4.1-2b-plus.
    Reconstructs absolute timestamps by unwrapping modulo-1000 rollovers.
    """
    if not ts_text or not ts_text.strip():
        return "", []

    ts_words = re.split(r"\[T:([^\]]+)\]", ts_text)
    last_word_end_time = 0.0
    offset_time = 0.0

    segments: List[ASRSegment] = []
    plain_words: List[str] = []

    for raw_word, ts_str in zip(ts_words[::2], ts_words[1::2]):
        word_text = raw_word.strip()
        if not ts_str:
            continue

        try:
            word_end_time = float(ts_str) / 100.0
        except ValueError:
            continue

        while word_end_time + offset_time < last_word_end_time:
            offset_time += 10.0
        absolute_end_time = word_end_time + offset_time
        
        absolute_start_time = last_word_end_time
        last_word_end_time = absolute_end_time

        if word_text == "_":
            continue

        if word_text:
            plain_words.append(word_text)
            word = ASRWord(
                start=absolute_start_time + chunk_offset,
                end=absolute_end_time + chunk_offset,
                text=word_text,
            )
            segments.append(
                ASRSegment(
                    start=word.start,
                    end=word.end,
                    text=word.text,
                    words=[word],
                )
            )

    plain_text = " ".join(plain_words)
    return plain_text, segments


def parse_granite_diarization(text: str, chunk_offset: float, chunk_duration: float) -> Tuple[str, List[ASRSegment]]:
    """
    Parses speaker-attributed ASR text generated by ibm-granite/granite-speech-4.1-2b-plus.
    Returns:
        plain_text: Cleaned text without speaker tags.
        segments: List of ASRSegment instances with speaker property populated.
    """
    if not text or not text.strip():
        return "", []

    # Find speaker tags like [Speaker 1]:, [Speaker 2]:, etc.
    # Keep the delimiter matching group so we can reconstruct who is speaking.
    pattern = r"\[(Speaker \d+)\]:?"
    parts = re.split(pattern, text)
    
    segments: List[ASRSegment] = []
    
    # Handle text before the first speaker tag, if any
    first_text = parts[0].strip()
    if first_text:
        segments.append(
            ASRSegment(
                start=chunk_offset,
                end=chunk_offset + chunk_duration,
                text=first_text,
                speaker=None,
                words=[],
            )
        )
        
    for speaker, content in zip(parts[1::2], parts[2::2]):
        segment_text = content.strip()
        if not segment_text:
            continue
        segments.append(
            ASRSegment(
                start=chunk_offset,
                end=chunk_offset + chunk_duration,
                text=segment_text,
                speaker=speaker,
                words=[],
            )
        )
        
    plain_text = " ".join(seg.text for seg in segments if seg.text).strip()
    plain_text = re.sub(r"\s+", " ", plain_text).strip()
    
    # Distribute the chunk_duration proportionally across segments based on character length
    total_len = sum(len(seg.text) for seg in segments if seg.text)
    if total_len > 0:
        current_time = chunk_offset
        for seg in segments:
            seg_len = len(seg.text) if seg.text else 0
            duration = chunk_duration * (seg_len / total_len)
            seg.start = current_time
            seg.end = current_time + duration
            current_time += duration

    return plain_text, segments


def format_granite_diarization_segments(segments: List[ASRSegment]) -> str:
    """
    Normalize Granite speaker attribution to the suite's canonical bracket format:
    [Speaker 1] text
    """
    parts: List[str] = []
    for seg in segments:
        text = (seg.text or "").strip()
        if not text:
            continue
        if seg.speaker:
            parts.append(f"[{seg.speaker}] {text}")
        else:
            parts.append(text)
    return " ".join(parts).strip()


def remap_words_to_speaker_segments(speaker_segments: List[ASRSegment], aligned_words: List[ASRWord]) -> None:
    """
    Distributes aligned words sequentially to speaker_segments based on text matching.
    """
    if not speaker_segments or not aligned_words:
        return

    word_idx = 0
    num_words = len(aligned_words)

    for segment in speaker_segments:
        # Tokenize segment text to find expected words
        expected_tokens = [w.lower() for w in re.findall(r"\b\w+\b", segment.text) if w]
        if not expected_tokens:
            continue
        
        segment_words = []
        match_count = 0
        limit = len(expected_tokens)
        
        while word_idx < num_words and match_count < limit:
            aligned_word = aligned_words[word_idx]
            aligned_token_clean = "".join(ch for ch in aligned_word.text.lower() if ch.isalnum())
            expected_token_clean = "".join(ch for ch in expected_tokens[match_count] if ch.isalnum())
            
            if not aligned_token_clean:
                segment_words.append(aligned_word)
                word_idx += 1
                continue
                
            if (aligned_token_clean == expected_token_clean or 
                    expected_token_clean in aligned_token_clean or 
                    aligned_token_clean in expected_token_clean):
                segment_words.append(aligned_word)
                word_idx += 1
                match_count += 1
            elif (match_count + 1 < limit and 
                  ("".join(ch for ch in expected_tokens[match_count + 1] if ch.isalnum()) in aligned_token_clean or
                   aligned_token_clean in "".join(ch for ch in expected_tokens[match_count + 1] if ch.isalnum()))):
                # Missed an expected word in alignment, skip the expected word and match current aligned to next expected
                match_count += 2
                segment_words.append(aligned_word)
                word_idx += 1
            else:
                # Mismatch, consume to avoid getting stuck
                segment_words.append(aligned_word)
                word_idx += 1
                match_count += 1

        if segment_words:
            segment.words = segment_words
            segment.start = segment_words[0].start
            segment.end = segment_words[-1].end
            
    # If there are leftover words, append them to the last segment that had some words
    if word_idx < num_words and speaker_segments:
        last_seg = speaker_segments[-1]
        if last_seg.words:
            last_seg.words.extend(aligned_words[word_idx:])
            last_seg.end = aligned_words[-1].end
        else:
            last_seg.words = aligned_words[word_idx:]
            last_seg.start = aligned_words[word_idx].start
            last_seg.end = aligned_words[-1].end


class GraniteASREngineAdapter:
    GRANITE_LANGUAGES = {
        "english": "English",
        "french": "French",
        "german": "German",
        "spanish": "Spanish",
        "portuguese": "Portuguese",
        "japanese": "Japanese",
    }

    QWEN_ALIGNER_LANGUAGES = {
        "Chinese",
        "English",
        "Cantonese",
        "French",
        "German",
        "Italian",
        "Japanese",
        "Korean",
        "Portuguese",
        "Russian",
        "Spanish",
    }

    GENERIC_SPACE_ALIGNER_LANGUAGE = "English"

    def __init__(self, engine_data: Dict[str, Any]):
        self.engine_data = engine_data
        self.config = engine_data.get("config", engine_data)

    def _normalize_language(self, language: Optional[str]) -> Optional[str]:
        if not language or str(language).lower() == "auto":
            return None
        normalized = self.GRANITE_LANGUAGES.get(str(language).strip().lower())
        model_name = self.config.get("model_name", "")
        if normalized == "Japanese" and "plus" in str(model_name).lower():
            return None
        return normalized

    def _get_model(self):
        from utils.models.unified_model_interface import unified_model_interface, ModelLoadConfig
        from utils.device import resolve_torch_device

        device = self.config.get("device", "auto")
        if model_management.cpu_mode():
            device = "cpu"
        elif device != "cpu":
            device = resolve_torch_device(device)

        config = ModelLoadConfig(
            engine_name="granite_asr",
            model_type="asr",
            model_name=self.config.get("model_name", "granite-speech-4.1-2b"),
            model_path=self.config.get("model_name", "granite-speech-4.1-2b"),
            device=device,
            additional_params={
                "precision": self.config.get("dtype", "auto"),
                "attn_implementation": self.config.get("attn_implementation", "auto"),
            },
        )
        return unified_model_interface.load_model(config)

    def _get_forced_aligner(self):
        from utils.models.unified_model_interface import unified_model_interface, ModelLoadConfig
        from utils.models.factory_config import RUNTIME_MODE_SHARED
        from utils.device import resolve_torch_device

        device = self.config.get("device", "auto")
        if model_management.cpu_mode():
            device = "cpu"
        elif device != "cpu":
            device = resolve_torch_device(device)

        config = ModelLoadConfig(
            engine_name="qwen3_asr",
            model_type="aligner",
            model_name="Qwen3-ForcedAligner-0.6B",
            model_path="Qwen3-ForcedAligner-0.6B",
            device=device,
            runtime_mode=self.config.get("forced_aligner_runtime_mode", RUNTIME_MODE_SHARED),
            runtime_profile=self.config.get("forced_aligner_runtime_profile"),
            additional_params={
                "precision": self.config.get("dtype", "auto"),
                "attn_implementation": self.config.get("attn_implementation", "auto"),
            },
        )
        return unified_model_interface.load_model(config)

    def _prepare_audio(self, audio: Dict[str, Any]) -> torch.Tensor:
        waveform = audio["waveform"]
        sample_rate = audio["sample_rate"]

        if waveform.ndim == 3:
            waveform = waveform[0]
        if waveform.shape[0] > 1:
            waveform = torch.mean(waveform, dim=0)
        else:
            waveform = waveform[0]

        if sample_rate != 16000:
            import torchaudio.functional as TA_F
            waveform = TA_F.resample(waveform, orig_freq=sample_rate, new_freq=16000)
        return waveform

    def _alignment_requested(self, req: ASRRequest) -> bool:
        return req.timestamps == "word"

    def _can_use_aligner(self, req: ASRRequest, normalized_language: Optional[str], warnings: List[str]) -> bool:
        if not self._alignment_requested(req):
            return False

        if req.task != "transcribe":
            warnings.append("Granite timestamps skipped: Qwen forced aligner only works for transcription, not translation.")
            return False

        if not self.config.get("asr_use_forced_aligner", False):
            warnings.append("Granite timestamps skipped: Qwen forced aligner is disabled on the Granite engine node.")
            return False

        if normalized_language and normalized_language not in self.QWEN_ALIGNER_LANGUAGES:
            warnings.append(f"Granite timestamps skipped: Qwen forced aligner does not support '{normalized_language}'.")
            return False

        return True

    def _append_once(self, messages: List[str], message: str) -> None:
        if message not in messages:
            messages.append(message)

    def _contains_japanese_script(self, text: str) -> bool:
        for ch in text:
            code = ord(ch)
            if 0x3040 <= code <= 0x309F:  # Hiragana
                return True
            if 0x30A0 <= code <= 0x30FF:  # Katakana
                return True
            if 0x31F0 <= code <= 0x31FF:  # Katakana Phonetic Extensions
                return True
            if 0xFF66 <= code <= 0xFF9D:  # Halfwidth Katakana
                return True
            if 0x3400 <= code <= 0x4DBF:  # CJK Unified Ideographs Extension A
                return True
            if 0x4E00 <= code <= 0x9FFF:  # CJK Unified Ideographs
                return True
            if 0xF900 <= code <= 0xFAFF:  # CJK Compatibility Ideographs
                return True
        return False

    def _resolve_aligner_language(
        self,
        normalized_language: Optional[str],
        transcript: str,
        notes: List[str],
    ) -> str:
        if normalized_language:
            return normalized_language

        if self._contains_japanese_script(transcript):
            self._append_once(
                notes,
                "Granite Auto alignment used a Japanese-script heuristic, so Qwen forced aligner ran in Japanese mode. This is tokenizer routing for alignment, not Granite native language detection.",
            )
            return "Japanese"

        self._append_once(
            notes,
            "Granite Auto alignment used Qwen's generic space-delimited tokenizer path. 'English' here is only the aligner mode for non-Japanese Granite languages, not detected spoken language.",
        )
        return self.GENERIC_SPACE_ALIGNER_LANGUAGE

    def _align_words(
        self,
        aligner,
        chunk_audio,
        transcript: str,
        language: str,
        chunk_offset: float,
        warnings: List[str],
    ) -> List[ASRSegment]:
        if not transcript.strip():
            return []

        try:
            results = aligner.align(audio=[(chunk_audio, 16000)], text=[transcript], language=[language])
            if not results:
                return []

            segments: List[ASRSegment] = []
            for item in results[0]:
                word = ASRWord(
                    start=float(item.start_time) + chunk_offset,
                    end=float(item.end_time) + chunk_offset,
                    text=str(item.text),
                )
                segments.append(
                    ASRSegment(
                        start=word.start,
                        end=word.end,
                        text=word.text,
                        words=[word],
                    )
                )
            return segments
        except Exception as e:
            warnings.append(f"Granite timestamps skipped: Qwen forced aligner failed ({e}).")
            return []

    def _run_chunk(
        self,
        runtime,
        chunk_np,
        req: ASRRequest,
        normalized_language: Optional[str],
        target_language: Optional[str],
        translate_instruction_override: Optional[str],
        timestamps: bool = False,
        diarization: bool = False,
    ) -> Tuple[str, Optional[str]]:
        generation_kwargs = {
            "max_new_tokens": int(self.config.get("max_new_tokens", req.max_new_tokens or 200)),
            "do_sample": bool(self.config.get("do_sample", False)),
            "num_beams": int(self.config.get("num_beams", 1)),
            "temperature": float(self.config.get("temperature", 1.0)),
            "top_k": int(self.config.get("top_k", 50)),
            "top_p": float(self.config.get("top_p", 1.0)),
            "repetition_penalty": float(self.config.get("repetition_penalty", 1.0)),
            "length_penalty": float(self.config.get("length_penalty", 1.0)),
            "no_repeat_ngram_size": int(self.config.get("no_repeat_ngram_size", 0)),
            "early_stopping": bool(self.config.get("early_stopping", False)),
        }
        result = runtime.transcribe(
            chunk_np,
            task=req.task,
            language=normalized_language,
            target_language=target_language,
            translate_instruction_override=translate_instruction_override,
            generation_kwargs=generation_kwargs,
            timestamps=timestamps,
            diarization=diarization,
        )
        return result.get("text", ""), result.get("language")

    def transcribe(self, req: ASRRequest) -> ASRResult:
        model_management.throw_exception_if_processing_interrupted()

        runtime = self._get_model()
        waveform = self._prepare_audio(req.audio)
        wav_np = waveform.cpu().numpy()
        sample_rate = 16000

        warnings: List[str] = []
        notes: List[str] = []
        normalized_language = self._normalize_language(req.language)
        model_name = self.config.get("model_name", "granite-speech-4.1-2b")
        is_plus = "plus" in str(model_name).lower()
        
        diarization_requested = is_plus and req.diarization and req.task == "transcribe"
        if req.diarization:
            if not is_plus:
                warnings.append("Granite diarization skipped: Only 'plus' variants support speaker attribution natively.")
            elif req.task != "transcribe":
                warnings.append("Granite diarization skipped: Speaker attribution is only supported for transcription, not translation.")

        target_language = self.config.get("asr_translate_target_language", "English")
        translate_instruction_override = str(
            self.config.get("asr_translate_instruction_override", DEFAULT_TRANSLATE_INSTRUCTION_TEMPLATE)
            or DEFAULT_TRANSLATE_INSTRUCTION_TEMPLATE
        ).strip()
        if req.language and str(req.language).lower() != "auto" and not normalized_language:
            if str(req.language).lower() == "japanese" and "plus" in str(model_name).lower():
                warnings.append(
                    "Granite Speech Plus variant does not support Japanese. Falling back to Granite auto language handling."
                )
            else:
                warnings.append(
                    f"Granite received unsupported language hint '{req.language}'. Falling back to Granite auto language handling."
                )
        if req.task == "translate":
            self._append_once(
                notes,
                f"Granite translation target is '{target_language}'. This target is prompt-driven by the Granite engine node, not a native model target-language parameter.",
            )
            if translate_instruction_override != DEFAULT_TRANSLATE_INSTRUCTION_TEMPLATE:
                self._append_once(
                    notes,
                    "Granite custom translation instruction override is active on the engine node. This is still wrapped in Granite's chat format, but malformed or weak instructions can fall back to plain transcription.",
                )

        use_native_timestamps = is_plus and req.timestamps == "word" and req.task == "transcribe" and not diarization_requested
        use_aligner = not use_native_timestamps and self._can_use_aligner(req, normalized_language, warnings)
        aligner = self._get_forced_aligner() if use_aligner else None

        chunk_size = int(req.chunk_size)
        overlap = int(req.overlap)
        total_samples = len(wav_np)

        segments: List[ASRSegment] = []
        full_text_parts: List[str] = []
        output_language = normalized_language

        if chunk_size > 0:
            chunk_samples = chunk_size * sample_rate
            overlap_samples = overlap * sample_rate
            step_samples = max(1, chunk_samples - overlap_samples)
            num_chunks = max(1, int((total_samples - overlap_samples + step_samples - 1) / step_samples))

            chunk_progress_bar = comfy.utils.ProgressBar(num_chunks)
            start_time = time.time()
            last_print = 0.0

            for i in range(num_chunks):
                model_management.throw_exception_if_processing_interrupted()
                start = i * step_samples
                end = min(start + chunk_samples, total_samples)
                chunk_np = wav_np[start:end]
                text, detected_language = self._run_chunk(
                    runtime,
                    chunk_np,
                    req,
                    normalized_language,
                    target_language,
                    translate_instruction_override,
                    timestamps=use_native_timestamps,
                    diarization=diarization_requested,
                )
                if detected_language and not output_language:
                    output_language = detected_language

                if diarization_requested:
                    chunk_offset = start / sample_rate
                    parsed_text, chunk_segments = parse_granite_diarization(text, chunk_offset, len(chunk_np) / sample_rate)
                    if parsed_text:
                        full_text_parts.append(format_granite_diarization_segments(chunk_segments))
                    
                    if aligner is not None and parsed_text:
                        aligner_language = self._resolve_aligner_language(normalized_language, parsed_text, notes)
                        word_segments = self._align_words(
                            aligner=aligner,
                            chunk_audio=chunk_np,
                            transcript=parsed_text,
                            language=aligner_language,
                            chunk_offset=chunk_offset,
                            warnings=warnings,
                        )
                        aligned_words = [seg.words[0] for seg in word_segments if seg.words]
                        remap_words_to_speaker_segments(chunk_segments, aligned_words)
                        
                    for seg in chunk_segments:
                        if seg.speaker:
                            seg.text = f"[{seg.speaker}] {seg.text}"
                    
                    segments.extend(chunk_segments)
                elif use_native_timestamps:
                    chunk_offset = start / sample_rate
                    parsed_text, chunk_segments = parse_granite_native_timestamps(text, chunk_offset)
                    if parsed_text:
                        full_text_parts.append(parsed_text)
                    segments.extend(chunk_segments)
                else:
                    if text:
                        full_text_parts.append(text)
                    if aligner is not None and text:
                        aligner_language = self._resolve_aligner_language(normalized_language, text, notes)
                        chunk_offset = start / sample_rate
                        segments.extend(
                            self._align_words(
                                aligner=aligner,
                                chunk_audio=chunk_np,
                                transcript=text,
                                language=aligner_language,
                                chunk_offset=chunk_offset,
                                warnings=warnings,
                            )
                        )

                try:
                    chunk_progress_bar.update(1)
                    now = time.time()
                    if now - last_print >= 0.5 or (i + 1) == num_chunks:
                        elapsed = now - start_time
                        its = (i + 1) / elapsed if elapsed > 0 else 0.0
                        eta = (num_chunks - (i + 1)) / its if its > 0 else 0.0
                        bar_width = 12
                        filled = int(((i + 1) / num_chunks) * bar_width)
                        bar = f"[{'█' * filled}{'░' * (bar_width - filled)}]"
                        print(f"\r   Progress: {bar} {i+1}/{num_chunks} | {its:.1f} it/s | {elapsed:.0f}s | ETA {eta:.0f}s", end="", flush=True)
                        last_print = now
                except Exception:
                    pass

            try:
                elapsed = time.time() - start_time
                avg = (num_chunks / elapsed) if elapsed > 0 else 0.0
                print(f"\r   Complete: {num_chunks} chunks in {elapsed:.1f}s (avg {avg:.1f} it/s)" + " " * 20)
            except Exception:
                pass
        else:
            text, detected_language = self._run_chunk(
                runtime,
                wav_np,
                req,
                normalized_language,
                target_language,
                translate_instruction_override,
                timestamps=use_native_timestamps,
                diarization=diarization_requested,
            )
            output_language = detected_language or output_language

            if diarization_requested:
                parsed_text, chunk_segments = parse_granite_diarization(text, 0.0, len(wav_np) / sample_rate)
                if parsed_text:
                    full_text_parts.append(format_granite_diarization_segments(chunk_segments))
                
                if aligner is not None and parsed_text:
                    aligner_language = self._resolve_aligner_language(normalized_language, parsed_text, notes)
                    word_segments = self._align_words(
                        aligner=aligner,
                        chunk_audio=wav_np,
                        transcript=parsed_text,
                        language=aligner_language,
                        chunk_offset=0.0,
                        warnings=warnings,
                    )
                    aligned_words = [seg.words[0] for seg in word_segments if seg.words]
                    remap_words_to_speaker_segments(chunk_segments, aligned_words)
                    
                for seg in chunk_segments:
                    if seg.speaker:
                        seg.text = f"[{seg.speaker}] {seg.text}"
                
                segments.extend(chunk_segments)
            elif use_native_timestamps:
                parsed_text, chunk_segments = parse_granite_native_timestamps(text, 0.0)
                if parsed_text:
                    full_text_parts.append(parsed_text)
                segments.extend(chunk_segments)
            else:
                if text:
                    full_text_parts.append(text)
                if aligner is not None and text:
                    aligner_language = self._resolve_aligner_language(normalized_language, text, notes)
                    segments.extend(
                        self._align_words(
                            aligner=aligner,
                            chunk_audio=wav_np,
                            transcript=text,
                            language=aligner_language,
                            chunk_offset=0.0,
                            warnings=warnings,
                        )
                    )

        text = " ".join(part for part in full_text_parts if part).strip()
        raw = None
        if warnings or notes:
            raw = {}
            if warnings:
                raw["warnings"] = warnings
            if notes:
                raw["notes"] = notes
        return ASRResult(text=text, language=output_language, segments=segments, raw=raw)
